// TODO remove, this demo shouldn't need to reset the theme.

const defaultTheme = createTheme();

const backgroundImageUrls = [
  "frontend-dinner-designer/public/foodImages/image1.jpg",
  "frontend-dinner-designer/public/foodImages/image2.jpg",
  "frontend-dinner-designer/public/foodImages/image3.jpg"
];

const getRandomImageUrl = () => {
  return backgroundImageUrls[Math.floor(Math.random() * backgroundImageUrls.length)];
};

export default function SignInSide() {
  const bgUrl = getRandomImageUrl();
  const navigate = useNavigate(); 
  const [errorMessage, setErrorMessage] = useState('');

  const handleSubmit = async(event) => {
    event.preventDefault();
    const data = new FormData(event.currentTarget);
    const email = data.get('email');
    const password = data.get('password');
    const rememberMe = data.get('remember');

    try{
      const response = await fetch('http://127.0.0.1:5000/auth/login', {
          method: 'POST',
          headers: {
              'Content-Type': 'application/json',
          },
          body: JSON.stringify({ email, password, rememberMe}),
      });
      
      if (response.ok) {
          const data = await response.json();
          const token = data.access_token;

          sessionStorage.setItem('token', token); // Store token in sessionStorage for security reasons
          alert('Login successful!');
          navigate('/auth/dashboard');
      } else {
          const errorData = await response.json();
          setErrorMessage(errorData.message || 'Login failed. Please try again.');
          alert(errorMessage);
      }
  } catch (error) {
      setErrorMessage('Network error. Please try again.');
      console.error('Error:', error);
  }
  };

  return (
    <ThemeProvider theme={defaultTheme}>
      <Grid container component="main" sx={{ height: '100vh' }}>
        <CssBaseline />
        <Grid
          item
          xs={false}
          sm={4}
          md={7}
          sx={{
            backgroundImage: `url("${bgUrl}")`,
            backgroundColor: (t) =>
              t.palette.mode === 'light' ? t.palette.grey[50] : t.palette.grey[900],
            backgroundSize: 'cover',
            backgroundPosition: 'left',
          }}
        />
        <Grid item xs={12} sm={8} md={5} component={Paper} elevation={6} square>
          <Box
            sx={{
              my: 8,
              mx: 4,
              display: 'flex',
              flexDirection: 'column',
              alignItems: 'center',
            }}
          >
            <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
              <LockOutlinedIcon />
            </Avatar>
            <Typography component="h1" variant="h5">
              Sign in
            </Typography>
            <Box component="form" noValidate onSubmit={handleSubmit} sx={{ mt: 1 }}>
              <TextField
                margin="normal"
                required
                fullWidth
                id="email"
                label="Email Address"
                name="email"
                autoComplete="email"
                autoFocus
              />
              <TextField
                margin="normal"
                required
                fullWidth
                name="password"
                label="Password"
                type="password"
                id="password"
                autoComplete="current-password"
              />
              <FormControlLabel
                control={<Checkbox value="remember" color="primary" />}
                label="Remember me"
              />
              <Button
                type="submit"
                fullWidth
                variant="contained"
                sx={{ mt: 3, mb: 2 }}
              >
                Sign In
              </Button>
              <Grid container>
                <Grid item xs>
                  <Link href="#" variant="body2">
                    Forgot password?
                  </Link>
                </Grid>
                <Grid item>
                  <Link href="/auth/register" variant="body2">
                    {"Don't have an account? Sign Up"}
                  </Link>
                </Grid>
              </Grid>
              <Copyright sx={{ mt: 5 }} />
            </Box>
          </Box>
        </Grid>
      </Grid>
    </ThemeProvider>
  );
}
